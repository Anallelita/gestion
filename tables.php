  <h1 align="center">Energia</h1>
  
  La energia solar es el calor y la luz que irradia el sol, controla el clima de la Tierra y permite la existencia de seres vivos en nuestro planeta.
Se denomina energía a la propiedad que le permite a los objetos físicos realizar algún trabajo.
Todos los cambios o transformaciones de la naturaleza -que puede percibir el hombre- son producidos por algún tipo de energía. Podemos afirmar entonces que la energía es la fuente de todo movimiento.
 
